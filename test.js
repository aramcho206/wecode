function evenNumber(numbers) {
    for (var i=0; i<50; i++) {
        if (numbers[i] === 0) {
                console.log(numbers[i] +  " is even");
        }
        else if (numbers[i] % 2 === 0) {
                console.log(numbers[i] + " is even");   
        }
        else {
                console.log(numbers[i] + " is odd");
        }
}
}

 